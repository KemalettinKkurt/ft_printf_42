#include <stdio.h>

int	ft_strlen(int a)
{
	int	i;
	i = 0;
	if (a < 0)
	{
		a = -a;
		i = 1;
	}
	
	if(a == 0)
		return 1;

	
	while (a > 0)
	{
		a = a / 10;
		i++;
	}
		
	return (i);
}
